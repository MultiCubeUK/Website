        For keeping chunks loaded, always or just when you are playing, we use our internally developed plugin, called <a href="https://github.com/cnordbakk/AnotherChunkLoader" target="_new">AnotherChunkLoader</a>.</span><br /><span style="color: #000000;">Everyone is given the possibility of loading 1 chunk while playing (called a Personal Chunk), and you can donate for additional chunks in our donation store.</span></p>
        <br />
        <p><span style="color: #000000;">To start using the plugin and force load a chunk, type "/acl" in chat. This will give you a wooden stick (it may look ordinary, but it really is magical!). With this stick in hand, right click wherever you want the chunk loader placed. This will bring up a inventory menu where you have to select between a Personal Loader or a World Loader (always loaded, even when you are not online). Clicking one of them will bring up a new menu where you select how many chunks you want to load. The stack size represents the radius, and the tooltip when hovering a stack reveals how many chunks will be loaded. Clicking one of them will, provided you have enough available chunks, place a Iron Block (Personal) or a Diamond Block (world) to represent the location of the loader, and the selected number of chunks will now be loaded. You right click this block with the (magic) stick to resize it. If you break the block the chunks will unload and the block will be destroyed. Breaking the block also returns the chunks to your available balance.</span></p>
        <br />
        <p><span style="color: #000000;">Other commands:</span><br /><span style="color: #000000;"><strong>/acl balance</strong> - Display the number of used and total chunks available for loading.</span><br /><span style="color: #000000;"><strong>/act delete &lt;#|all&gt;</strong> - Delete a specified or all chunk loaders you own. The number to specify a specific loader is found using "/acl list".</span><br /><span style="color: #000000;"><strong>/acl help</strong> - List the available commands and their usage.</span><br /><span style="color: #000000;"><strong>/act list</strong> - List the type and location of all your placed chunk loaders.</span><br /><span style="color: #000000;"><strong>/acl place</strong> - Opens the same menu as right clicking with the stick, but will place the chunk loader at your feet position.</span>
        
